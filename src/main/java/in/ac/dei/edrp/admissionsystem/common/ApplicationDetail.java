package in.ac.dei.edrp.admissionsystem.common;

public class ApplicationDetail 
{

	private String appStatusCode;
	private String appStatusDesc;
	private String applicationNumber;
	private String universityID;
	
	private String vrfStatusCode;
	private String vrfStatusDesc;
	private String applicantName;
	private String dateOfBirth;
	private String fatherName;
	private String motherName;
	
	public ApplicationDetail() {
	
	}
	public String getAppStatusCode() {
		return appStatusCode;
	}
	public void setAppStatusCode(String appStatusCode) {
		this.appStatusCode = appStatusCode;
	}
	public String getAppStatusDesc() {
		return appStatusDesc;
	}
	public void setAppStatusDesc(String appStatusDesc) {
		this.appStatusDesc = appStatusDesc;
	}
	public String getApplicationNumber() {
		return applicationNumber;
	}
	public void setApplicationNumber(String applicationNumber) {
		this.applicationNumber = applicationNumber;
	}
	public String getUniversityID() {
		return universityID;
	}
	public void setUniversityID(String universityID) {
		this.universityID = universityID;
	}
	public String getVrfStatusCode() {
		return vrfStatusCode;
	}
	public void setVrfStatusCode(String vrfStatusCode) {
		this.vrfStatusCode = vrfStatusCode;
	}
	public String getVrfStatusDesc() {
		return vrfStatusDesc;
	}
	public void setVrfStatusDesc(String vrfStatusDesc) {
		this.vrfStatusDesc = vrfStatusDesc;
	}
	public String getApplicantName() {
		return applicantName;
	}
	public void setApplicantName(String applicantName) {
		this.applicantName = applicantName;
	}
	public String getDateOfBirth() {
		return dateOfBirth;
	}
	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}
	public String getFatherName() {
		return fatherName;
	}
	public void setFatherName(String fatherName) {
		this.fatherName = fatherName;
	}
	public String getMotherName() {
		return motherName;
	}
	public void setMotherName(String motherName) {
		this.motherName = motherName;
	}
	
	
	

}
